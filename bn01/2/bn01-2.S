    .section    .text
    .globl      fill_buffer

fill_buffer:
    # Аргументы функции:
    #   %edi - указатель на буфер (char * buf)

    movb    $'H', (%edi)      # Записываем 'H' в первый байт буфера
    movb    $'e', 1(%edi)     # Записываем 'e' во второй байт буфера
    movb    $'l', 2(%edi)     # Записываем 'l' в третий байт буфера
    movb    $'l', 3(%edi)     # Записываем 'l' в четвертый байт буфера
    movb    $'o', 4(%edi)     # Записываем 'o' в пятый байт буфера

    ret
